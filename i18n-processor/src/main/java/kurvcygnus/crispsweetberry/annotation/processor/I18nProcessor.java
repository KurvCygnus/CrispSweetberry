package kurvcygnus.crispsweetberry.annotation.processor;

import kurvcygnus.crispsweetberry.annotations.AutoI18n;
import org.jetbrains.annotations.Contract;
import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;

import javax.annotation.processing.*;
import javax.lang.model.SourceVersion;
import javax.lang.model.element.Element;
import javax.lang.model.element.ElementKind;
import javax.lang.model.element.TypeElement;
import javax.lang.model.type.TypeMirror;
import javax.lang.model.util.Elements;
import javax.lang.model.util.Types;
import javax.tools.Diagnostic;
import javax.tools.StandardLocation;
import java.io.*;
import java.nio.charset.StandardCharsets;
import java.nio.file.Files;
import java.util.*;
import java.util.function.BinaryOperator;
import java.util.regex.Matcher;

import static kurvcygnus.crispsweetberry.annotations.AutoI18n.Lang;
import static kurvcygnus.crispsweetberry.annotations.AutoI18n.TRANSLATION_PATTERN;

